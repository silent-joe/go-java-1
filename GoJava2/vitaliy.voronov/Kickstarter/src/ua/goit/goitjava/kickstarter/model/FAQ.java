package ua.goit.goitjava.kickstarter.model;

public class FAQ {
	private String quastion;
	private String answer;
	
	public FAQ(String quastion, String answer) {
		this.quastion = quastion;
		this.answer = "No answer";
	}

	public String getQuastion() {
		return quastion;
	}

	public String getAnswer() {
		return answer;
	}
	
	
	
	
	
	
	
	
	
}
